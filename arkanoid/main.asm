; waitVblank -- Espera hasta VBLANK
waitvb:           
   ld a,[$FF44] ; rLY
   cp 144
   jr nz, waitvb
   ret 

; waitNtimesVb -- Espera N veces VBLANK
; 
; Input:
; a -- Número de veces a esperar a vblank
;
; Output:
; Nada
;
waitNvb:
.ffor: ;for(a=n,a>0,a--)
   push af
   call waitvb
   pop af

   dec a
   cp 0
   jr nz, .ffor

   ret

